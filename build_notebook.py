                "class ProgressBar:\n",
                "    def __init__(self, bar_width=50):\n",
                "        self.bar_width = bar_width\n",
                "        self.period = None\n",
                "        self.item = 0\n",
                "    def start(self, count):\n",
                "        self.item = 0\n",
                "        self.period = max(1, int(count / self.bar_width))\n",
                "        sys.stdout.write(\"[\" + (\" \" * self.bar_width) + \"]\")\n",
                "        sys.stdout.flush()\n",
                "        sys.stdout.write(\"\\b\" * (self.bar_width + 1))\n",
                "    def tick(self):\n",
                "        if self.item > 0 and self.item % self.period == 0:\n",
                "            sys.stdout.write(\"-\")\n",
                "            sys.stdout.flush()\n",
                "        self.item += 1\n",
                "    def stop(self):\n",
