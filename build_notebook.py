                "def get_dictionary(corpus, special_tokens, threshold=2):\n",
                "    dictionary = {}\n",
                "    for s in corpus:\n",
                "        for w in s:\n",
                "            dictionary[w] = dictionary.get(w, 0) + 1\n",
                "    words = special_tokens + [w for w in sorted(dictionary) if dictionary[w] > threshold]\n",
