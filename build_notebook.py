                "def preprocess_transliteration(text):\n",
                "    if not isinstance(text, str):\n",
                "        return \"\"\n",
                "    \n",
                "    # 1. Remove modern scribal notations\n",
                "    text = re.sub(r'[!?/:\\.\\(\\)\u02fd\u02fe]', '', text)\n",
                "    text = text.replace('<', '').replace('>', '')\n",
                "    text = text.replace('[', '').replace(']', '')\n",
                "    \n",
                "    # 2. Replace breaks, gaps, superscripts, subscripts\n",
                "    text = text.replace('x', '<gap>')\n",
                "    text = text.replace('...', '<big_gap>')\n",
                "    \n",
                "    # 3. Specific character substitutions (H-substitutions)\n",
                "    text = text.replace('\u1e2b', 'h').replace('\u1e2a', 'H')\n",
                "    \n",
                "    # Normalize spaces\n",
                "    text = re.sub(r'\\s+', ' ', text).strip()\n",
                "    return text\n",
